import 'dart:ui';

class Plant {
  final String id;
  final String name;
  final String imageUrl;
  final Color backgroundColor;
  final double price;
  final String type;
  final String category;
  final double light;
  final double water;
  final double temp;
  final String description;

  Plant({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.backgroundColor,
    required this.price,
    required this.type,
    required this.category,
    required this.light,
    required this.water,
    required this.temp,
    required this.description,
  });

  
}


